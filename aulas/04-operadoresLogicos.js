// Operadores Lógicos
let moroEmBariri = true;

console.log("Moro na cidade de Bariri", moroEmBariri);
console.log("Moro na cidade de Bariri", !moroEmBariri);

let manha = true;
let sono = false;

// Comparador E (AND) e OU (OR)
console.log("-------------------")
console.log("Operador and (e): ", manha && sono);
console.log("Operador or (ou): ", manha || sono);

// Operador E (AND) necessita que as duas comparações sejam verdadeiras para que ele aceite

// Operador OU (OR) necessita de apenas uma como verdadeira